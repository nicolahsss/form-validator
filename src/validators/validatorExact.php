namespace Validator\validators;

use Validator\ValidatorInterface;

/**
 * Description of modelMin
 *
 * @author nicolahsss
 */
class validatorExact implements validatorInterface {

    private $value;
    private $option;
    private $error = null;

    public function setValue($value): void {
        $this->value = $value;
    }

    public function setOption(string $option): void {
        $this->option = $option;
    }

    public function execute() {
        if ((!\is_null($this->value)) && (\strlen($this->value) != $this->option)) {
            $this->error = "must contain exactly {$this->option} characters";
            return false;
        }
        return $this->value;
    }

    public function error() {
        return $this->error;
    }

}
